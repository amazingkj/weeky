func dumpReports(db *sql.DB, w func(string, ...any)) int {
	// 신규 스키마 먼저 시도, 실패하면 레거시
	rows, err := db.Query(`SELECT id, user_id, team_name, author_name, report_date,
		this_week, next_week, issues, notes, next_issues, next_notes, template_id, created_at
		FROM reports ORDER BY id`)
	fullSchema := true
	if err != nil {
		rows, err = db.Query(`SELECT id, team_name, author_name, report_date,
			this_week, next_week, issues, template_id, created_at
			FROM reports ORDER BY id`)
		fullSchema = false
		if err != nil {
			log.Printf("  Skip reports: %v", err)
			return 0
		}
	}
	defer rows.Close()

	w("-- Data: reports")
	var count int
	for rows.Next() {
		var id, userID, templateID int64
		var teamName, authorName, reportDate, thisWeek, nextWeek string
		var issues, notes, nextIssues, nextNotes, createdAt string
		if fullSchema {
			if err := rows.Scan(&id, &userID, &teamName, &authorName, &reportDate,
				&thisWeek, &nextWeek, &issues, &notes, &nextIssues, &nextNotes, &templateID, &createdAt); err != nil {
				log.Printf("  Error scanning report: %v", err)
				continue
			}
		} else {
			if err := rows.Scan(&id, &teamName, &authorName, &reportDate,
				&thisWeek, &nextWeek, &issues, &templateID, &createdAt); err != nil {
				log.Printf("  Error scanning report: %v", err)
				continue
			}
		}
		w("INSERT INTO reports (id, user_id, team_name, author_name, report_date, this_week, next_week, issues, notes, next_issues, next_notes, template_id, created_at) VALUES (%d, %d, '%s', '%s', '%s', %s, %s, %s, %s, %s, %s, %d, %s);",
			id, userID, esc(teamName), esc(authorName), esc(reportDate),
			strVal(thisWeek), strVal(nextWeek), strVal(issues), strVal(notes),
			strVal(nextIssues), strVal(nextNotes), templateID, ts(createdAt))
		count++
	}
	w("")
	log.Printf("  reports: %d rows", count)
	return count
}

