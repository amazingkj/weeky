func dumpConsolidatedEdits(db *sql.DB, w func(string, ...any)) int {
	rows, err := db.Query("SELECT id, team_id, report_date, data, updated_by, updated_at FROM consolidated_edits ORDER BY id")
	if err != nil {
		log.Printf("  Skip consolidated_edits: %v", err)
		return 0
	}
	defer rows.Close()

	w("-- Data: consolidated_edits")
	var count int
	for rows.Next() {
		var id, teamID, updatedBy int64
		var reportDate, data, updatedAt string
		if err := rows.Scan(&id, &teamID, &reportDate, &data, &updatedBy, &updatedAt); err != nil {
			log.Printf("  Error scanning consolidated_edit: %v", err)
			continue
		}
		w("INSERT INTO consolidated_edits (id, team_id, report_date, data, updated_by, updated_at) VALUES (%d, %d, '%s', %s, %d, %s);",
			id, teamID, esc(reportDate), strVal(data), updatedBy, ts(updatedAt))
		count++
	}
	w("")
	log.Printf("  consolidated_edits: %d rows", count)
	return count
}
